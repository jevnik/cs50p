def test_drop_piece_ok():
    board = [
        ['','','','','','','',],
        ['','','','','','','',],
        ['','','','','','','',],
        ['','','','','','','',],
        ['','','','','','','',],
        ['','','','','','','',],
    ]
    assert drop_piece((40,100),board) == 0

